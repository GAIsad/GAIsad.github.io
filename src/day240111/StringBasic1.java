package day240111;

public class StringBasic1 {
    public static void main(String[] args) {
        String msg="Hello World";
        System.out.println(msg);

        String single="a";
        String emptyString="b";

        //char sc="";
        System.out.println(single+"=="+emptyString);

        System.out.println("=== === ===");

        String prefix="百搭";
        //byte/short/int/long float/double char boolean

        byte test1 =1;
        System.out.println(prefix + test1);

        short test2 =2;
        System.out.println(prefix + test2);

        int test3 = 4;
        System.out.println(prefix + test3);

        long test4 =8L;
        System.out.println(prefix + test4);

        float test5 =1;
        System.out.println(prefix + test5);

        double test6 = 2.2;
        System.out.println(prefix + test6);

        char test7 = 'a';
        System.out.println(prefix + test7);

        boolean test8 =true;
        System.out.println(prefix + test8);




    }
}
